public class Matrix {
    // Instance variables
    private int size;
    
    // 0-arg constructor
    public Matrix() {
      size = 1;
    }
    
    // 1-arg constructor
    public Matrix(int size) {
      this.size = size;
    }
    
    public static void main(String[] args) {
      System.out.println("Hello World!");
    }
}